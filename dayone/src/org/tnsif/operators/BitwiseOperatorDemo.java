//program to demonstrate on bitwise & shifting operator
package org.tnsif.operators;

public class BitwiseOperatorDemo {

	public static void main(String[] args) {
		int x=56, y=5;
		System.out.println(x&y);
		System.out.println(x|y);
		System.out.println(x^y);
		int a=10, b=2;
		System.out.println(a<<b);
		System.out.println(a>>b);

	}

}
